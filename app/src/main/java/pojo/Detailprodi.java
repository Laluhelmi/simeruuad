public class Detailprodi {
    private String jam;
    private String dosen;
    private String matkulprodi;
    private String ruang;
    private String kelas;

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public String getDosen() {
        return dosen;
    }

    public void setDosen(String dosen) {
        this.dosen = dosen;
    }

    public String getMatkulprodi() {
        return matkulprodi;
    }

    public void setMatkulprodi(String matkulprodi) {
        this.matkulprodi = matkulprodi;
    }

    public String getRuang() {
        return ruang;
    }

    public void setRuang(String ruang) {
        this.ruang = ruang;
    }

    public String getKelas() {
        return kelas;
    }

    public void setKelas(String kelas) {
        this.kelas = kelas;
    }
}
